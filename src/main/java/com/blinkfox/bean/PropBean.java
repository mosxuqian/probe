package com.blinkfox.bean;

/**
 * 用于测试各种情况下的属性信息的JavaBean.
 * Created by blinkfox on 2017/6/17.
 */
public class PropBean {

    private byte myByte;

    private short myShort;

    private int myInt;

    private long myLong;

    private float myFloat;

    private double myDouble;

    private boolean myBool;

    private char myChar;

    private Byte myWarpByte;

    private Short myWarpShort;

    private Integer myWarpInt;

    private Long myWarpLong;

    private Float myWarpFloat;

    private Double myWarpDouble;

    private Boolean myWarpBool;

    private Character myWarpChar;

    /**
     * 默认构造方法.
     */
    public PropBean() {
        super();
    }

    /* getter和setter方法. */
    public byte getMyByte() {
        return myByte;
    }

    public void setMyByte(byte myByte) {
        this.myByte = myByte;
    }

    public short getMyShort() {
        return myShort;
    }

    public void setMyShort(short myShort) {
        this.myShort = myShort;
    }

    public int getMyInt() {
        return myInt;
    }

    public void setMyInt(int myInt) {
        this.myInt = myInt;
    }

    public long getMyLong() {
        return myLong;
    }

    public void setMyLong(long myLong) {
        this.myLong = myLong;
    }

    public float getMyFloat() {
        return myFloat;
    }

    public void setMyFloat(float myFloat) {
        this.myFloat = myFloat;
    }

    public double getMyDouble() {
        return myDouble;
    }

    public void setMyDouble(double myDouble) {
        this.myDouble = myDouble;
    }

    public boolean isMyBool() {
        return myBool;
    }

    public void setMyBool(boolean myBool) {
        this.myBool = myBool;
    }

    public char getMyChar() {
        return myChar;
    }

    public void setMyChar(char myChar) {
        this.myChar = myChar;
    }

    public Byte getMyWarpByte() {
        return myWarpByte;
    }

    public void setMyWarpByte(Byte myWarpByte) {
        this.myWarpByte = myWarpByte;
    }

    public Short getMyWarpShort() {
        return myWarpShort;
    }

    public void setMyWarpShort(Short myWarpShort) {
        this.myWarpShort = myWarpShort;
    }

    public Integer getMyWarpInt() {
        return myWarpInt;
    }

    public void setMyWarpInt(Integer myWarpInt) {
        this.myWarpInt = myWarpInt;
    }

    public Long getMyWarpLong() {
        return myWarpLong;
    }

    public void setMyWarpLong(Long myWarpLong) {
        this.myWarpLong = myWarpLong;
    }

    public Float getMyWarpFloat() {
        return myWarpFloat;
    }

    public void setMyWarpFloat(Float myWarpFloat) {
        this.myWarpFloat = myWarpFloat;
    }

    public Double getMyWarpDouble() {
        return myWarpDouble;
    }

    public void setMyWarpDouble(Double myWarpDouble) {
        this.myWarpDouble = myWarpDouble;
    }

    public Boolean getMyWarpBool() {
        return myWarpBool;
    }

    public void setMyWarpBool(Boolean myWarpBool) {
        this.myWarpBool = myWarpBool;
    }

    public Character getMyWarpChar() {
        return myWarpChar;
    }

    public void setMyWarpChar(Character myWarpChar) {
        this.myWarpChar = myWarpChar;
    }

}